package com.stepupit.reminder.web.model;

public class Contact {
	
	private String name;
	private String dob;
	private String anniversaryDob;
	private String phone;
	private String email;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDob() {
		return dob;
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getAnniversaryDob() {
		return anniversaryDob;
	}
	public void setAnniversaryDob(String anniversaryDob) {
		this.anniversaryDob = anniversaryDob;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	

}
